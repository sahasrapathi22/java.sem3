public class incriment {
    public static void main(String[] args) {
        int x=30;
        System.out.println("original:"+x);
        System.out.println("post incriment:"+(x++));
        System.out.println("after post incriment:"+x);
        System.out.println("pre incrimennt :"+(++x));
        System.out.println("post decrement:"+(x--));
        System.out.println("pre incriment:"+(--x));

    }
    
}
